function Transfer() {
}

Transfer.numberTransferCharacter = function(number) {
    switch (number) {
        case 0 :
            return '';
            break;
        case 1 :
            return '一';
            break;
        case 2 :
            return '二';
            break;
        case 3 :
            return '三';
            break;
        case 4 :
            return '四';
            break;
        case 5 :
            return '五';
            break;
        case 6 :
            return '六';
            break;
        case 7 :
            return '七';
            break;
        case 8 :
            return '八';
            break;
        case 9 :
            return '九';
            break;
    }
};
